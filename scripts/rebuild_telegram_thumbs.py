def rebuild(stripped: bytes) -> bytes:
    if len(stripped) < 3 or stripped[0] != 1:
        return stripped

    header = JPEG_HEADER.copy()
    header[164] = stripped[1]
    header[166] = stripped[2]
    return bytes(header) + stripped[3:] + b"\xff\xd9"


def main() -> None:
    DESTINATION.mkdir(parents=True, exist_ok=True)
    entries = json.loads(SOURCE.read_text(encoding="utf-8"))

    for entry in entries:
        stripped = base64.b64decode(entry["stripped_base64"])
        output = DESTINATION / f"{entry['slug']}.jpg"
        output.write_bytes(rebuild(stripped))
        print(output.relative_to(ROOT))


if __name__ == "__main__":
    main()
